export enum ExternalTokenFetchStyle {
  CONTRACT = 1,
  ID = 2,
  BOTH = 3,
  NONE = 4,
  COIN_MARKET_CAP = 5,
  SWAP_PAIRS = 6,
}
export class ExternalToken implements IExternalToken {
  chain: ChainNetwork;
  id: string;
  name: string;
  contractAddr: string;
  decimals: number;
  coingeckoId: string;
  fetchType?: ExternalTokenFetchStyle;
  swapPairs?: string[];
  isNativeToken?: boolean;
  price?: number;
  constructor(
    chain: ChainNetwork,
    id: string,
    name: string,
    cgid: string,
    addr: string,
    dec = 18,
    style: ExternalTokenFetchStyle = ExternalTokenFetchStyle.BOTH,
    swapPairs: string[] = [],
    isNativeToken = false,
  ) {
    this.chain = chain;
    this.id = id;
    this.name = name;
    this.coingeckoId = cgid;
    this.contractAddr = addr;
    this.decimals = dec;
    this.fetchType = style;
    this.swapPairs = swapPairs;
    this.isNativeToken = isNativeToken;
  }
  setPrice(val: number): void {
    this.price = val;
  }
  toOutputFormat(): IExternalToken {
    const ret: IExternalToken = {
      chain: this.chain,
      id: this.id,
      contractAddr: this.contractAddr,
      decimals: this.decimals,
    };
    if (this.price) {
      ret.price = this.price;
    }
    if (this.name) {
      ret.name = this.name;
    }
    return ret;
  }
}

